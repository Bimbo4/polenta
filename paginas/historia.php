            <p class="serif">Restaurante Polenta es una marca registrada ante el Instituto Mexicano de la Propiedad
Industrial, lo que le garantiza su exclusividad en la República Mexicana, esta ventaja permitirá en un mediano plazo, ofertar franquicias a nivel regional y en el largo plazo a nivel nacional. En el 2008, la empresa decide que es tiempo de iniciar con una expansión más seria, por lo cual, realizan varios estudios de mercado, considerando la necesidad de ciudades aledañas, así como su
capacidad para captar posibles inversionistas. De esta manera, deciden abrir la primera unidad
bajo la marca registrada POLENTA, en la ciudad de Tizayuca. Con una economía basada en su zona
industrial, conformada por empresas reconocidas a nivel nacional e internacional, brinda el
espacio adecuado para la marca por lo variado de sus visitantes, entre ejecutivos y clientes
industriales.
El 27 de marzo del 2008 fue inaugurado el primer eslabón de la que esperan sea una gran cadena,
para este inicio, se aposto en una de las regiones con mayor crecimiento en nuestro estado, en
Tizayuca se programó iniciar esta aventura empresarial , creando 45 nuevas fuentes de empleo, de los cuales el 90% son personas residentes de Tizayuca, con un nivel de ingreso
ligeramente superior al mercado local, creando un número igual de empleos indirectos al dar
prioridad a los proveedores locales.
El 09 de junio de 2014 Restaurante Polenta se convierte en Franquicia de los Restaurantes Mirage,
comenzando una nueva etapa con una administración enfocada a la mejora continua en todos los
aspectos siguiendo enfocados a captar más clientes.
En 2015 Restaurante Polenta Tizayuca obtiene tres sellos otorgados por SECTUR Federal: Punto
Limpio, Distintivo M y Distintivo H los cuales han sido muy importantes para que Restaurante
Polenta Tizayuca sea el Restaurante más importante de la zona.</p>
